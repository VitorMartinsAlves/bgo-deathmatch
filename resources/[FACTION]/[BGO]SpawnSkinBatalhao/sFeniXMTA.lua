function pickupskin ( thePlayer )
    local accName = getAccountName(getPlayerAccount(thePlayer))
    if exports.bgo_dashboard:isPlayerInFaction(thePlayer , 2) or ( isObjectInACLGroup ("user."..accName, aclGetGroup ( "Console" ) ) ) then
     
 setPedSkin ( thePlayer, 284 )
    end
end
addEventHandler ( "onPickupUse", skin, pickupskin )


